'''
Problema: Escribe un programa que solicite la base y la altura de un rectángulo y calcule su área.
'''


# Solicitar al usuario la base y la altura del rectángulo
base = float(input("Ingrese la base del rectángulo: "))
altura = float(input("Ingrese la altura del rectángulo: "))

# Calcular el área del rectángulo
area = base * altura

# Mostrar el resultado
print(f"El área del rectángulo es: {area}")

# Usamos la función input()para que el usuario ingrese la base y la altura del rectángulo. Dado que estos valores pueden ser números decimales, los convertimos a tipo floatpara permitir entradas con decimales.

# Calculamos el área del rectángulo utilizando la fórmula Área = base * altura.

# Finalmente, mostramos el resultado con un mensaje.

